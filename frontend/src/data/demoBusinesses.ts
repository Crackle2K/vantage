function cloneBusiness(business: Business): Business {
  return {
    ...business,
    image_urls: [...(business.image_urls ?? [])],
    known_for: [...(business.known_for ?? [])],
    badges: [...(business.badges ?? [])]
  };
}

export function getDemoBusinesses(): Business[] {
  return demoBusinesses.map(cloneBusiness);
}

export function buildDemoExploreLanes(items: Business[]): ExploreLane[] {
  const active = items.filter((business) => business.is_active_today);
  const trusted = items.filter((business) => (business.trust_score ?? 0) >= 88);
  const hiddenGems = items.filter((business) => (business.review_count ?? 0) < 190 && (business.local_confidence ?? 0) >= 0.82);

  return [
    {
      id: 'all',
      title: 'Browse All',
      subtitle: 'Premium Toronto demo businesses with live trust signals',
      items
    },
    {
      id: 'active',
      title: 'Active Right Now',
      subtitle: 'Verified visits and fresh local momentum',
      items: active
    },
    {
      id: 'trusted',
      title: 'Most Trusted',
      subtitle: 'High credibility scores and strong recent reviews',
      items: trusted
    },
    {
      id: 'hidden_gems',
      title: 'Hidden Gems',
      subtitle: 'Smaller independents with unusually strong local confidence',
      items: hiddenGems
    }
  ].filter((lane) => lane.items.length > 0);
}

